#include<iostream>

// source - http://www.bartleby.com/113/1001.html
// Emily Dickinson - Part One: Life

using namespace std;

int main()
{
    cout << "SUCCESS is counted sweetest\n";
    cout << "By those who ne'er succeed\n";
    cout << "To comprehend a nectar\n";
    cout << "Requires sorest need.\n";
    cout << "\n";

    cout << "Not one of all the purple host\n";
    cout << "Who took the flag to-day\n";
    cout << "Can tell the definition,\n";
    cout << "So clear, of victory,\n";
    cout << "\n";

    cout << "As he, defeated, dying,\n";
    cout << "On whose forbidden ear\n";
    cout << "The distant strains of triumph\n";
    cout << "Break, agonized and clear.\n";

    return 0;
}
